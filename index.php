    <script>
        const themeStylesheet = document.querySelector('#themeStylesheet');

        const themeSelectors = document.querySelectorAll('.theme-selector .theme-types a');
        
        const onThemeChange = e => {
            console.log(e)
            themeStylesheet.setAttribute('href', e.target.getAttribute('data-theme-url'))
        }

        themeSelectors.forEach(e => e.addEventListener('click',onThemeChange))

        const achievementsSection = document.querySelector('.achievements');

        const nav = document.querySelector('nav')

        const html = document.querySelector('html');

        const navToggle = document.querySelector('.nav-toggle');

        const mobileNav = document.querySelector('nav ul');

        const onWindowScroll = e => {
            if(window.scrollY > (achievementsSection.offsetHeight )){
                html.style.backgroundImage = 'url(../static/img/bg/bg3.jpg)'
            }else{
                html.style.backgroundImage = 'url(../static/img/bg/bg001.jpeg)'

            }

            /* ADD AN ACTIVE CLASS TO NAV TO CHANGE BACKGROUND COLOR */
            if (window.scrollY > 100) {
                nav.classList.add('active')
            }else{
                nav.classList.remove('active')
            }
        }

        const toggleNav = () => {
            if(navToggle.classList.contains('active')){
                mobileNav.classList.remove('mobile-active')
                return navToggle.classList.remove('active')

            }

            mobileNav.classList.add('mobile-active')
            return navToggle.classList.add('active')

        }

        document.addEventListener('scroll',onWindowScroll)

        navToggle.addEventListener('click', toggleNav);
    </script>
